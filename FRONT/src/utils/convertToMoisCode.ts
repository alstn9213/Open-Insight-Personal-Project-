export const convertToMoisCode = (admCode: string): string => {
  // 7자리인 경우 변환 수행
  if (admCode.length === 7) {
    const kostatGuCode = admCode.substring(0, 5); // 앞 5자리 (구 코드)
    const dongCode = admCode.substring(5);        // 뒤 2자리 (동 코드)
    
    if (KOSTAT_TO_MOIS_GU[kostatGuCode]) {
      // 매핑된 구 코드 + 동 코드 + '0'
      return KOSTAT_TO_MOIS_GU[kostatGuCode] + dongCode + "0";
    }
    // 매핑 정보가 없으면 뒤에 0만 붙임
    return admCode + "0";
  }
  // 이미 8자리 이상이면 8자리까지만 자름
  return admCode.substring(0, 8);
};